package cn.seventeen.framework.asm.struts.util.exception;

public class ByteArrayConvertException extends Exception{

    public ByteArrayConvertException() {
    }

    public ByteArrayConvertException(String message) {
        super(message);
    }

    public ByteArrayConvertException(String message, Throwable cause) {
        super(message, cause);
    }

    public ByteArrayConvertException(Throwable cause) {
        super(cause);
    }

    public ByteArrayConvertException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }

}
